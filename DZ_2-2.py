a = ['в', '5', 'часов', '17', 'минут', 'температура', 'воздуха', 'была', '+5', 'градусов']
hour = int(input('Укажите часы  '))
minute = int(input('Укажите минуты  '))
temp = int(input('Укажите температуру  '))
if temp <= 0:
    print(f'{a[0]} "{hour:02}" {a[2]} "{minute:02}" {a[4]} {a[5]} {a[6]} {a[7]} "{temp:02}" {a[9]}')
else:
    print(f'{a[0]} "{hour:02}" {a[2]} "{minute:02}" {a[4]} {a[5]} {a[6]} {a[7]} "+{temp:02}" {a[9]}')
